<script>
  export let style = "loadingBar";

  const loadingBar = [
    "[...............]",
    "[#..............]",
    "[##.............]",
    "[###............]",
    "[####...........]",
    "[#####..........]",
    "[######.........]",
    "[#######........]",
    "[########.......]",
    "[#########......]",
    "[##########.....]",
    "[###########....]",
    "[############...]",
    "[#############..]",
    "[##############.]",
    "[###############]",
    "[.##############]",
    "[..#############]",
    "[...############]",
    "[....###########]",
    "[.....##########]",
    "[......#########]",
    "[.......########]",
    "[........#######]",
    "[.........######]",
    "[..........#####]",
    "[...........####]",
    "[............###]",
    "[.............##]",
    "[..............#]",
    "[...............]",
  ];

  const animations = {
    loadingBar: loadingBar,
  };

  if (!animations[style]) {
    style = "loadingBar";
  }

  let count = 0;

  const incrementCount = () => {
    setInterval(() => {
      count += 1;
      if (count >= animations[style].length) {
        count = 0;
      }
    }, 30);
  };

  incrementCount();
</script>

<div>
  <p class="animation-text">{animations[style][count]}</p>
</div>
